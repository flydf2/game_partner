export function mockGetRewardOrders(params = {}) {
  return new Promise((resolve) => {
    setTimeout(() => {
      let filteredOrders = [...mockRewardOrders]

      if (params.game) {
        filteredOrders = filteredOrders.filter(order => order.game === params.game)
      }

      if (params.status) {
        filteredOrders = filteredOrders.filter(order => order.status === params.status)
      }

      if (params.paymentMethod) {
        filteredOrders = filteredOrders.filter(order => order.paymentMethod === params.paymentMethod)
      }

      if (params.keyword) {
        const keyword = params.keyword.toLowerCase()
        filteredOrders = filteredOrders.filter(order => 
          order.content.toLowerCase().includes(keyword) ||
          order.tags.some(tag => tag.toLowerCase().includes(keyword)) ||
          order.game.toLowerCase().includes(keyword)
        )
      }

      const page = params.page || 1
      const pageSize = params.pageSize || 20
      const startIndex = (page - 1) * pageSize
      const endIndex = startIndex + pageSize
      const paginatedData = filteredOrders.slice(startIndex, endIndex)

      resolve({
        success: true,
        data: paginatedData,
        pagination: {
          currentPage: page,
          totalPages: Math.ceil(filteredOrders.length / pageSize),
          totalCount: filteredOrders.length
        }
      })
    }, 300)
  })
}


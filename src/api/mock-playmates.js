export function mockGetPlaymates(params = {}) {
  return new Promise((resolve) => {
    setTimeout(() => {
      let filteredPlaymates = [...mockPlaymates]

      if (params.game) {
        filteredPlaymates = filteredPlaymates.filter(p => p.game === params.game)
      }

      if (params.online !== undefined) {
        filteredPlaymates = filteredPlaymates.filter(p => p.isOnline === params.online)
      }

      if (params.priceRange) {
        const [min, max] = params.priceRange.split('-').map(Number)
        if (params.priceRange === '100+') {
          filteredPlaymates = filteredPlaymates.filter(p => p.price >= 100)
        } else {
          filteredPlaymates = filteredPlaymates.filter(p => p.price >= min && p.price <= max)
        }
      }

      if (params.rank) {
        filteredPlaymates = filteredPlaymates.filter(p => p.rank === params.rank)
      }

      if (params.gender) {
        filteredPlaymates = filteredPlaymates.filter(p => p.gender === params.gender)
      }

      if (params.keyword) {
        const keyword = params.keyword.toLowerCase()
        filteredPlaymates = filteredPlaymates.filter(p => 
          p.nickname.toLowerCase().includes(keyword) ||
          (Array.isArray(p.tags) ? p.tags.some(tag => tag.toLowerCase().includes(keyword)) : p.tags.toLowerCase().includes(keyword))
        )
      }

      if (params.sortBy) {
        switch (params.sortBy) {
          case 'rating':
            filteredPlaymates.sort((a, b) => b.rating - a.rating)
            break
          case 'price_asc':
            filteredPlaymates.sort((a, b) => a.price - b.price)
            break
          case 'price_desc':
            filteredPlaymates.sort((a, b) => b.price - a.price)
            break
          case 'newest':
            filteredPlaymates.reverse()
            break
        }
      }

      const page = params.page || 1
      const pageSize = params.pageSize || 20
      const startIndex = (page - 1) * pageSize
      const endIndex = startIndex + pageSize
      const paginatedData = filteredPlaymates.slice(startIndex, endIndex)

      resolve({
        code: 0,
        data: {
          data: paginatedData,
          pagination: {
            currentPage: page,
            totalPages: Math.ceil(filteredPlaymates.length / pageSize),
            totalCount: filteredPlaymates.length
          }
        },
        msg: '获取成功'
      })
    }, 300)
  })
}

